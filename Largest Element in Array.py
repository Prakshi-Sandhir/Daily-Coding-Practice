# https://www.geeksforgeeks.org/problems/largest-element-in-array4009/1
class Solution:
    def largest(self, arr):
        lar=arr[0]
        for i in range(len(arr)-1):
            if lar<arr[i+1]:
                lar=arr[i+1]
        return lar
                    
    
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
        
            
                    
                
                
        
